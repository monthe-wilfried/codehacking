    public function createReply(CommentRepliesRequest $request)
    {
        //
        $user = Auth::user();

        $data = [
            'comment_id' => $request->comment_id,
            'author' => $user->name,
            'email' => $user->email,
            'body' => $request->reply,
            'photo' => $user->photo->file
        ];

        CommentReply::create($data);

        return redirect()->back()->with('success', 'Reply posted!');

    }

